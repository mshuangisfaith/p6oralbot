function part(title, tag, chunks, moves, frames, recallData) {
  return {
    title,
    tag,
    chunks: chunks.map(([keyword, text]) => ({ keyword, text })),
    moves,
    frames,
    recall: recallData
  };
}

function recall(question, options, answer) {
  return { question, options, answer };
}

function chunkKey(questionId, partIndex, chunkIndex) {
  return `${questionId}:${partIndex}:${chunkIndex}`;
}

function currentQuestions() {
  return lessonData.filter((item) => item.set === state.set);
}

function currentQuestion() {
  return currentQuestions()[state.questionIndex];
}

function currentPart() {
  return currentQuestion().parts[state.partIndex];
}

function render() {
  renderTabs();
  renderQuestionList();
  renderWorkspace();
  renderProgress();
}

function renderTabs() {
  const sets = [...new Set(lessonData.map((item) => item.set))];
  els.setTabs.innerHTML = "";
  sets.forEach((set) => {
    const button = document.createElement("button");
    button.className = `tab-button ${set === state.set ? "active" : ""}`;
    button.type = "button";
    button.textContent = set;
    button.addEventListener("click", () => {
      state.set = set;
      state.questionIndex = 0;
      state.partIndex = 0;
      stopSpeech();
      render();
    });
    els.setTabs.append(button);
  });
}

function renderQuestionList() {
  els.questionList.innerHTML = "";
  currentQuestions().forEach((question, index) => {
    const button = document.createElement("button");
    button.className = `question-button ${index === state.questionIndex ? "active" : ""}`;
    button.type = "button";
    button.innerHTML = `<strong>${question.short}</strong><small>${question.question}</small>`;
    button.addEventListener("click", () => {
      state.questionIndex = index;
      state.partIndex = 0;
      stopSpeech();
      render();
    });
    els.questionList.append(button);
  });
}

function renderWorkspace() {
  const question = currentQuestion();
  const partData = currentPart();
  const revealedChunks = partData.chunks.filter((_, index) => state.unlocked.has(chunkKey(question.id, state.partIndex, index)));

  els.setLabel.textContent = `${question.set} · Question ${question.short}`;
  els.questionTitle.textContent = question.question;
  els.questionFocus.textContent = question.focus;
  els.paragraphTitle.textContent = partData.title;
  els.partCount.textContent = `${state.partIndex + 1} / ${question.parts.length}`;
  els.prevPart.disabled = state.partIndex === 0;
  els.nextPart.disabled = state.partIndex === question.parts.length - 1;
  els.recallFeedback.textContent = "";

  renderKeywords(question, partData);
  renderResponse(question, partData);
  renderMoves(partData);
  renderFrames(partData);
  renderRecall(partData, revealedChunks.length === partData.chunks.length);
}

function renderKeywords(question, partData) {
  els.keywordBank.innerHTML = "";
  partData.chunks.forEach((chunk, index) => {
    const isSelected = state.unlocked.has(chunkKey(question.id, state.partIndex, index));
    const button = document.createElement("button");
    button.className = `keyword-button ${isSelected ? "selected" : ""}`;
    button.type = "button";
    button.textContent = chunk.keyword;
    button.addEventListener("click", () => {
      state.unlocked.add(chunkKey(question.id, state.partIndex, index));
      renderWorkspace();
      renderProgress();
      readText(chunk.text);
    });
    els.keywordBank.append(button);
  });
}

