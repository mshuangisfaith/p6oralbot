function renderRecall(partData, isFullyRevealed) {
  els.recallQuestion.textContent = partData.recall.question;
  els.recallOptions.innerHTML = "";
  els.recallOptions.classList.toggle("locked", !isFullyRevealed);
  partData.recall.options.forEach((option, index) => {
    const button = document.createElement("button");
    button.className = "recall-button";
    button.type = "button";
    button.textContent = option;
    button.addEventListener("click", () => {
      if (!isFullyRevealed) {
        els.recallFeedback.textContent = "Reveal all keyword parts first. Then come back to choose the best answer.";
        els.recallFeedback.classList.remove("nudge");
        void els.recallFeedback.offsetWidth;
        els.recallFeedback.classList.add("nudge");
        return;
      }
      [...els.recallOptions.children].forEach((child, childIndex) => {
        child.disabled = true;
        if (childIndex === partData.recall.answer) child.classList.add("correct");
      });
      if (index !== partData.recall.answer) button.classList.add("wrong");
      els.recallFeedback.textContent = index === partData.recall.answer ? "Correct. Now try saying the idea aloud using a sentence frame." : "Not quite. Look back at the revealed response parts and try the next recall check carefully.";
    });
    els.recallOptions.append(button);
  });
  if (!isFullyRevealed) {
    els.recallFeedback.textContent = "Reveal all keyword parts first, then try the recall check.";
  }
}

function renderProgress() {
  const total = lessonData.reduce((sum, item) => sum + item.parts.reduce((partSum, section) => partSum + section.chunks.length, 0), 0);
  const done = state.unlocked.size;
  els.progressText.textContent = `${done} / ${total}`;
  els.progressBar.style.width = `${Math.round((done / total) * 100)}%`;
}

function readText(text) {
  if (!els.speechToggle.checked || !("speechSynthesis" in window)) return;
  stopSpeech();
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.rate = 0.88;
  utterance.pitch = 1;
  utterance.lang = "en-SG";
  window.speechSynthesis.speak(utterance);
}

function stopSpeech() {
  if ("speechSynthesis" in window) window.speechSynthesis.cancel();
}

els.prevPart.addEventListener("click", () => {
  if (state.partIndex > 0) {
    state.partIndex -= 1;
    stopSpeech();
    renderWorkspace();
  }
});

els.nextPart.addEventListener("click", () => {
  if (state.partIndex < currentQuestion().parts.length - 1) {
    state.partIndex += 1;
    stopSpeech();
    renderWorkspace();
  }
});

els.readCurrent.addEventListener("click", () => {
  const question = currentQuestion();
  const partData = currentPart();
  const visibleText = partData.chunks
    .filter((_, index) => state.unlocked.has(chunkKey(question.id, state.partIndex, index)))
    .map((chunk) => chunk.text)
    .join(" ");
  readText(visibleText || "Choose a keyword to reveal part of the model response.");
});

els.stopSpeech.addEventListener("click", stopSpeech);
els.speechToggle.addEventListener("change", () => {
  if (!els.speechToggle.checked) stopSpeech();
});

